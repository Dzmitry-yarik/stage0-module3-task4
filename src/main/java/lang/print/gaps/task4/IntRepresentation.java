package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
         int q = 99;
        int w = 104;
        int e = 97;
        int r = 114;
        char z = (char) q;
        char x = (char) w;
        char c = (char) e;
        char v = (char) r;
        System.out.println(z);
        System.out.println(x);
        System.out.println(c);
        System.out.println(v);
    }
}
